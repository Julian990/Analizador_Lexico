    private void analizarLexico() throws IOException {
        int cont = 1;
        
        String expr = (String) txtResultado.getText();
        Lexer lexer = new Lexer(new StringReader(expr));
        String resultado = "LINEA " + cont + "\t\tSIMBOLO\n";
        while (true) {
            Tokens token = lexer.yylex();
            if (token == null) {
                txtAnalizarLex.setText(resultado);
                return;
            }
            switch (token) {
                case Linea:
                    cont++;
                    resultado += "LINEA " + cont + "\n";
                    break;
                case Comillas:
                    resultado += "  <Comillas>\t\t" + lexer.lexeme + "\n";
                    break;
                case Coma:
                    resultado += "  <Coma>\t\t" + lexer.lexeme + "\n";
                    break;
                case Punto:
                    resultado += "  <Punto>\t\t" + lexer.lexeme + "\n";
                    break;
                case string:
                case TipoDato:
                    resultado += "  <Tipo de dato>\t" + lexer.lexeme + "\n";
                    break;
                case If:
                case Else:
                case Package:
                case Public:
                case Class:
                case Return:
                case Do:
                case While:
                case For:
                case Try:
                case Catch:
                case exception:
                case Import:                
                case New:
                case Extends:
                case Super:
                case This:
                    resultado += "  <Reservada>\t\t" + lexer.lexeme + "\n";
                    break;
                case Asignacion:
                    resultado += "  <Operador de Asignacion>\t" + lexer.lexeme + "\n";
                    break;
                case OperadorAritmetico:
                    resultado += "  <Operador Aritmetico>\t" + lexer.lexeme + "\n";
                    break;
                case OperadorLogico:
                    resultado += "  <Operador Logico>\t\t" + lexer.lexeme + "\n";
                    break;
                case Incremento:
                    resultado += "  <Operador de Incremento>\t" + lexer.lexeme + "\n";
                    break;
                case Relacionales:
                    resultado += "  <Operador Relacional>\t" + lexer.lexeme + "\n";
                    break;
                case Atribucion:
                    resultado += "  <Operador atribucion>\t" + lexer.lexeme + "\n";
                    break;
                case Booleano:
                    resultado += "  <Operador booleano>\t" + lexer.lexeme + "\n";
                    break;
                case ParentecisA:
                    resultado += "  <Parentesis de apertura>\t" + lexer.lexeme + "\n";
                    break;
                case ParentecisC:
                    resultado += "  <Parentesis de cierre>\t" + lexer.lexeme + "\n";
                    break;
                case LlaveA:
                    resultado += "  <Llave de apertura>\t" + lexer.lexeme + "\n";
                    break;
                case LlaveC:
                    resultado += "  <Llave de cierre>\t" + lexer.lexeme + "\n";
                    break;
                case CorcheteA:
                    resultado += "  <Corchete de apertura>\t" + lexer.lexeme + "\n";
                    break;
                case CorcheteC:
                    resultado += "  <Corchete de cierre>\t" + lexer.lexeme + "\n";
                    break;
                case Main:
                    resultado += "  <Reservada main>\t" + lexer.lexeme + "\n";
                    break;
                case Static:
                    resultado += "  <Reservada static>\t" + lexer.lexeme + "\n";
                    break;
                case Void:
                    resultado += "  <Reservada void>\t" + lexer.lexeme + "\n";
                    break;
                case Private:
                    resultado += "  <Reservada private>\t" + lexer.lexeme + "\n";
                    break;                
                case FinalLinea:
                    resultado += "  <Final de linea>\t" + lexer.lexeme + "\n";
                    break;
                case Identificador:
                    resultado += "  <Identificador>\t\t" + lexer.lexeme + "\n";
                    break;
                case Numero:
                    resultado += "  <Numero>\t\t" + lexer.lexeme + "\n";
                    break;
                case ERROR:
                    resultado += lexer.lexeme + ": <Simbolo no definido>\n";
                    break;
                default:
                    resultado += "  < " + lexer.lexeme + " >\n";
                    break;
            }
        }
    }

